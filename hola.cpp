#include <iostream> // Required for standard input/output operations, specifically std::cout

// The main function, entry point of every C++ program
int main() {
    // Translate "escribe hola mundo" to print the string "hola mundo" to the console
    // std::cout is the standard output stream
    // << is the insertion operator, used to send data to the stream
    // "hola mundo" is the string literal to be printed
    // std::endl inserts a newline character and flushes the output buffer
    std::cout << "hola mundo" << std::endl;

    // Return 0 to indicate successful program execution
    return 0;
}
